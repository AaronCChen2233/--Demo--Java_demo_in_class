public class LengthOfString {
    public static void ShowStringLength(String str){
        System.out.println(str);
        System.out.println(str.length());
    }

    public static void ShowLowerCase(String str){
        System.out.println(str.toLowerCase());
        System.out.println(str.length());
    }

    public static void  ShowIndex(String str, String cstr){
        System.out.println(str.indexOf(cstr));
    }
}
